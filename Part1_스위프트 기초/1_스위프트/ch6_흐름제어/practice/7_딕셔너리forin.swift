// Dictionary
let freinds: [String: Int] = ["jay": 35, "joe": 29, "Jenny": 31]

for tuple in freinds {
    print(tuple)
}
// (key: "jay", value: 35)
// (key: "Jenny", value: 31)
// (key: "joe", value: 29)

// Set
let 지역번호: Set<String> = ["02", "031", "033", "041", "042", "043"]

for 번호 in 지역번호 {
    print(번호)
}
// 043
// 033
// 02
// ...
